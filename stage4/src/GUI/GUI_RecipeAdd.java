    private void addRecipeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRecipeButtonActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = this.tableItem.getSelectedRow();
        
        if (selectedRow != -1) {
            byte id = (byte) this.tableItem.getModel().getValueAt(selectedRow, 0);
            String name = String.valueOf(this.tableItem.getModel().getValueAt(selectedRow, 1));
            
            int qty = (int) this.spinnerItemQty.getValue();
            
            Item anItem = Inventory.getItem(id);
            
            if (this.food.getRecipe().containsKey(anItem)) {
                JOptionPane.showMessageDialog(null, "Item already exist as part of the recipe!");
            } else {
                this.food.addRecipe(anItem, (byte) qty);
            }
            
            this.dispose();
            
            this.guiRecipe.resetRowsRecipe();
            
            this.guiRecipe.populateTableRecipe();
            
            this.guiRecipe.setVisible(true);
        }
    }//GEN-LAST:event_addRecipeButtonActionPerformed

