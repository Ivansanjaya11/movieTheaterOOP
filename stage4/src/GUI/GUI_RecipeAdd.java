    public void populateItemRecipeTable() {

        DefaultTableModel itemRecipeTable = (DefaultTableModel) this.tableItem.getModel();
        
        ArrayList<Item> itemList = Inventory.getItemList();
                
        for (Item item : itemList) {
            Object[] aRow = new Object[2];
            
            byte id = item.getItemId();
            String name = item.getItemName();
            
            aRow[0] = id;
            aRow[1] = name;
            
            itemRecipeTable.addRow(aRow);
        }
    }
    
